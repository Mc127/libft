#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	m;
	char	c;

	m = n;
	if (m < 0)
	{
		m *= -1;
		write(fd, "-", 1);
	}
	if (m > 9)
	{
		ft_putnbr_fd(m / 10, fd);
		ft_putnbr_fd(m % 10, fd);
	}
	else
	{
		c = m + 48;
		write(fd, &c, 1);
	}
}
